
public class Slot {
	int slotId;
	String time;
	public Slot(int slotId, String time) {
		this.slotId = slotId;
		this.time = time;
	}
	public int getSlotId() {
		return slotId;
	}
	public void setSlotId(int slotId) {
		this.slotId = slotId;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
}
